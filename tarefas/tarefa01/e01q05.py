'''
Faça um programa que peça as 4 notas bimestrais de um aluno e mostre a média aritmética delas.
'''

bim1 = float(input('Informe a nota do 1º bimestre: '))
bim2 = float(input('Informe a nota do 2º bimestre: '))
bim3 = float(input('Informe a nota do 3º bimestre: '))
bim4 = float(input('Informe a nota do 4º bimestre: '))
media = (bim1 + bim2 + bim3 + bim4)/4
print(media)
